public class StringExercise {

    public static void main(String[] args) {

        String pinkFloydLyrics = "We don't need no education\n"
                + "We don't need no thought control\n";
        System.out.println(pinkFloydLyrics);

        String stringWithQuotes = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(stringWithQuotes);

        String singleBackslash = "In windows, the main drive is usually C:\\\n";
        System.out.println(singleBackslash);

        String backslashCombo = "I can do backslashes \\, \\\\,\nand the amazing triple backslash \\\\\\";
        System.out.println(backslashCombo);


    }

}
